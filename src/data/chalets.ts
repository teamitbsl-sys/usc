export function getChaletBySlug(slug: string): Chalet | undefined {
  return chalets.find(c => c.slug === slug);
}

export function getChaletsByResort(resortSlug: string): Chalet[] {
  return chalets.filter(c => c.resortSlug === resortSlug);
}

export function getChaletsByCountry(countrySlug: string): Chalet[] {
  return chalets.filter(c => c.countrySlug === countrySlug);
}

export function getChaletsByAmenity(amenity: string): Chalet[] {
  return chalets.filter(c =>
    c.amenities.some(a => a.toLowerCase().includes(amenity.toLowerCase()))
  );
}
